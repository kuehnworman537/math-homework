public class MathHomework {
    public static void main(String[] args) {
        // Example of how to use mathematical operations in Java
        int num1 = 5;
        int num2 = 3;
        double result = num1 + num2; // Adding two numbers
        System.out.println("The sum is: " + result);

        String str1 = "Hello, World!";
        String str2 = "Java"; // Concatenating two strings
        String resultString = str1 + str2; // Concatenating the strings with a plus sign

        System.out.println("The concatenated string is: " + resultString);
    }
}
