public class MathHomework {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
        double x = 5.0;
        int y = 10;
        float z = 7.8f;

        // Add two numbers
        double sum = x + y;
        System.out.println("Sum of " + x + " and " + y + " is: " + sum);

        // Multiply a number by another
        double product = x * y;
        System.out.println("Product of " + x + " and " + y + " is: " + product);

        // Subtract two numbers
        double difference = z - y;
        System.out.println("Difference of " + z + " and " + y + " is: " + difference);

        // Divide a number by another
        double quotient = x / y;
        System.out.println("Quotient of " + x + " and " + y + " is: " + quotient);
    }
}
