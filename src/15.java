public class MathHomework {
    public static void main(String[] args) {
        // Example of arithmetic operations and conditionals in Java
        int num1 = 5;
        int num2 = 3;
        double result;

        if (num1 > num2) {
            result = num1 + num2;
        } else {
            result = num1 - num2;
        }

        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + result);
    }
}
